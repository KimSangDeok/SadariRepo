package SadariModel;

public class ProjectRec {

	private String pjTitle;
	private String pjPrice;
	private String pjSt_dt;
	private String pjTerm;
	private String pjCategory;
	private String pjDeadline;
	
	public String getPjTitle() {
		return pjTitle;
	}
	public void setPjTitle(String pjTitle) {
		this.pjTitle = pjTitle;
	}
	public String getPjPrice() {
		return pjPrice;
	}
	public void setPjPrice(String pjPrice) {
		this.pjPrice = pjPrice;
	}
	public String getPjSt_dt() {
		return pjSt_dt;
	}
	public void setPjSt_dt(String pjSt_dt) {
		this.pjSt_dt = pjSt_dt;
	}
	public String getPjTerm() {
		return pjTerm;
	}
	public void setPjTerm(String pjTerm) {
		this.pjTerm = pjTerm;
	}
	public String getPjCategory() {
		return pjCategory;
	}
	public void setPjCategory(String pjCategory) {
		this.pjCategory = pjCategory;
	}
	public String getPjDeadline() {
		return pjDeadline;
	}
	public void setPjDeadline(String pjDeadline) {
		this.pjDeadline = pjDeadline;
	}
	
	
}
