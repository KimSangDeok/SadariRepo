package SadariModel;

public class ClientRec {
	
	private int ctNo;
	private String ctName;
	private String ctId;
	private String ctPassword;
	private String ctTel;
	private String ctIntroduce;
	
	
	public int getCtNo() {
		return ctNo;
	}
	public void setCtNo(int ctNo) {
		this.ctNo = ctNo;
	}
	public String getCtName() {
		return ctName;
	}
	public void setCtName(String ctName) {
		this.ctName = ctName;
	}
	public String getCtId() {
		return ctId;
	}
	public void setCtId(String ctId) {
		this.ctId = ctId;
	}
	public String getCtPassword() {
		return ctPassword;
	}
	public void setCtPassword(String ctPassword) {
		this.ctPassword = ctPassword;
	}
	public String getCtTel() {
		return ctTel;
	}
	public void setCtTel(String ctTel) {
		this.ctTel = ctTel;
	}
	public String getCtIntroduce() {
		return ctIntroduce;
	}
	public void setCtIntroduce(String ctIntroduce) {
		this.ctIntroduce = ctIntroduce;
	}
	
	
	
}